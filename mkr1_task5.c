#include <stdio.h>
#include <math.h> // Для sqrt
// Сама задача: Знайти радіус вписаного в трикутник кола, якщо відомі довжини його сторін
int main() {
    double a, b, c;

    // Введення довжин сторін трикутника
    printf("Введіть довжини сторін трикутника (a, b, c): ");
    scanf("%lf %lf %lf", &a, &b, &c);

    // Обчислення півпериметра
    double s = (a + b + c) / 2;

    // Перевірка чи трикутник існує (необов'язково, але корисно)
    if (a + b > c && a + c > b && b + c > a) {
        // Обчислення площі трикутника
        double area = sqrt(s * (s - a) * (s - b) * (s - c));

        // Обчислення радіуса вписаного кола
        double r = area / s;

        // Виведення результату
        printf("Радіус вписаного кола: %.6lf\n", r);
    } else {
        printf("Трикутник з такими сторонами не існує.\n");
    }

    return 0;
}
